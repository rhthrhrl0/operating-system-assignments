#define TIME_SLICE 10000000 //20182580
#define NULL ((void*)0) 	//20182580

//전역 변수로 weight를 놓음. 이건 프로세스 생성시마다 하나씩올려서 대입해줌.
int weight=1;	//20182580

struct {
  struct spinlock lock;
  struct proc proc[NPROC];
  // 가장 작은 priority값을 ptable이 관리.
  long long min_priority;	//20182580
} ptable;

static struct proc *initproc;

int nextpid = 1;
extern void forkret(void);
extern void trapret(void);

static void wakeup1(void *chan);

struct proc *ssu_schedule() //20182580
{
	struct proc *p;
	struct proc *ret=NULL;
	
    for(p = ptable.proc; p < &ptable.proc[NPROC]; p++){
      if(p->state != RUNNABLE)
        continue;
    	
	  // 러너블 상태에 priority값이 가장 작은 프로세스를 선택.
	  if(ret==NULL || (ret->priority > p->priority)){
	  	ret=p;
	  }
    }

#ifdef DEBUG
	// DEBUG매크로가 선언되어 있다면 실행할 코드
	if (ret)
		cprintf("PID: %d, NAME: %s, WEIGHT: %d, PRIORITY: %d\n", ret->pid, ret->name, ret->weight, ret->priority);
#endif
//전처리 지시자 끝.
	return ret;
}


void update_priority(struct proc *proc) //20182580
{
	proc->priority=proc->priority+(TIME_SLICE/proc->weight);
}


void update_min_priority() //20182580
{
	struct proc *min=NULL;
	struct proc *p;
		
    for(p = ptable.proc; p < &ptable.proc[NPROC]; p++){
      if(p->state != RUNNABLE)
        continue;
    	
	  // 러너블 상태에 priority값이 가장 작은 프로세스를 선택.
	  if(min==NULL || (min->priority > p->priority)){
	  	min=p;
	  }
    }

	if (min!=NULL)
			ptable.min_priority=min->priority;
}

// 프로세스가 생기거나, wake_up 하게 되는 경우. sleeping->runnable
void assign_min_priority(struct proc *proc) //20182580
{
	proc->priority=ptable.min_priority;
}

void
pinit(void)
{
  initlock(&ptable.lock, "ptable");
}

// Must be called with interrupts disabled
int
cpuid() {
  return mycpu()-cpus;
}

// Must be called with interrupts disabled to avoid the caller being
// rescheduled between reading lapicid and running through the loop.
struct cpu*
mycpu(void)
{
  int apicid, i;
  
  if(readeflags()&FL_IF)
    panic("mycpu called with interrupts enabled\n");
  
  apicid = lapicid();
  // APIC IDs are not guaranteed to be contiguous. Maybe we should have
  // a reverse map, or reserve a register to store &cpus[i].
  for (i = 0; i < ncpu; ++i) {
    if (cpus[i].apicid == apicid)
      return &cpus[i];
  }
  panic("unknown apicid\n");
}

// Disable interrupts so that we are not rescheduled
// while reading proc from the cpu structure
struct proc*
myproc(void) {
  struct cpu *c;
  struct proc *p;
  pushcli();
  c = mycpu();
  p = c->proc;
  popcli();
  return p;
}

